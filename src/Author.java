public class Author {
    public int id;
    public String nama_penulis;
    public String buku;

    public void inputData(int id, String nama_penulis, String buku) {
        this.id = id;
        this.nama_penulis = nama_penulis;
        this.buku = buku;
    }

    public void searchData(int id, String nama_penulis, String buku) {
        this.id = id;
        this.nama_penulis = nama_penulis;
        this.buku = buku;
    }

    public void collectData(int id, String nama_penulis, String buku) {
        this.id = id;
        this.nama_penulis = nama_penulis;
        this.buku = buku;
    }
}
